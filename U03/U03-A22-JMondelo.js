function aumentar() {
    var texto = document.getElementById("texto");
    texto.style.fontSize = "50px";
   }

   function disminuir() {
    var texto = document.getElementById("texto");
    texto.style.fontSize = "20px";

   }

   function restablecer() {
    var texto = document.getElementById("texto");
    texto.style.fontSize = "32px";
   }
